export const INDUSTRIES = Array.from(new Set(BUSINESSES.map((b) => b.industry))).sort();
export const COUNTRIES = Array.from(new Set(BUSINESSES.map((b) => b.country))).sort();
export const CITIES = Array.from(new Set(BUSINESSES.map((b) => b.city))).sort();
export const SIZES = ["Solo", "2-10", "11-50", "51-200", "200+"] as const;

export function getBusiness(slug: string) {
  return BUSINESSES.find((b) => b.slug === slug);
}

export function similarBusinesses(slug: string, limit = 3) {
  const me = getBusiness(slug);
  if (!me) return [];
  return BUSINESSES.filter((b) => b.slug !== slug && b.industry === me.industry).slice(0, limit);
}
